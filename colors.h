// Determines console colors for particular expressions
//
// If the command prompt does not allow any/some colors, 
// this may not work.

#define red     "\x1b[31m"
#define green   "\x1b[32m"
#define yellow  "\x1b[33m"
#define blue    "\x1b[34m"
#define magenta "\x1b[35m"
#define cyan    "\x1b[36m"
#define reset   "\x1b[0m" 
